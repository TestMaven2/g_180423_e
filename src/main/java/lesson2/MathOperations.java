package lesson2;

public class MathOperations {

    public static void main(String[] args) {

        int a = 3;
        int b = 9;
        int sum = a + b;
        System.out.println(sum);

        double c = 10;
        double d = 4;
        double result = c / d;
        System.out.println(result);

        System.out.println(10 % 3);
    }
}
